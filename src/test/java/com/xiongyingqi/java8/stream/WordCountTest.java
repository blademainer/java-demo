    private Map<String, Long> simpleWordCount(String word, String delimiter) {
        String[] split = word.split(delimiter);
        Map<String, Long> map = new HashMap<>();
        for (String s : split) {
            map.put(s, map.getOrDefault(s, 0L) + 1L);
        }
        return map;
    }
